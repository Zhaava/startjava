public class Variable {
	public static void main(String[] args) {
		byte core = 4;
		short ram = 16;
		int ssd = 256;
		long videoRam = 1536;
		float cpuGHz = 2.2f;
		double screenSize = 15.4;
		char macOrWindows= 'M';
		boolean macBook = true;

		System.out.println("Number of core: " + core);
		System.out.println("Amount of ram: " + ram);
		System.out.println("ssd: " + ssd);
		System.out.println("video ram: " + videoRam);
		System.out.println("cpu GHz: " + cpuGHz);
		System.out.println("screen size : " + screenSize);
		System.out.println("Mac or Win: " + macOrWindows);
		System.out.println("Working on macbook? : " + macBook);
	}
}